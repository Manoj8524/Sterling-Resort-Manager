var slugify = (s) => s.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
function generatePages(resort) {
	const out = [];
	out.push({
		slug: "master",
		layer: "master",
		layerLabel: "Resort Master",
		group: "master",
		groupLabel: "Master Page",
		title: `${resort.shortName} — Master Page`
	});
	for (const g of HYPERLOCAL) for (const subj of g.subjects) out.push({
		slug: `hyperlocal--${g.key}--${slugify(subj)}`,
		layer: "hyperlocal",
		layerLabel: "Hyperlocal",
		group: g.key,
		groupLabel: g.label,
		subject: subj,
		title: `${resort.shortName} — Hotels near ${subj}, ${resort.location}`
	});
	for (const g of GEO) out.push({
		slug: `geo--${g.key}`,
		layer: "geo",
		layerLabel: "GEO Intent",
		group: g.key,
		groupLabel: g.label,
		title: g.titleFor(resort)
	});
	for (const q of AEO) out.push({
		slug: `aeo--${q.key}`,
		layer: "aeo",
		layerLabel: "AEO Questions",
		group: q.key,
		groupLabel: q.question(resort),
		title: q.question(resort)
	});
	for (const g of GUIDES) out.push({
		slug: `guides--${g.key}`,
		layer: "guides",
		layerLabel: "Local Guides",
		group: g.key,
		groupLabel: g.label,
		title: g.titleFor(resort)
	});
	return out;
}
function findPage(resort, slug) {
	return generatePages(resort).find((p) => p.slug === slug);
}
